// 제품별 꿀팁 인덱스 관리
const TIP_INDEX_KEY = 'supp_tip_index';

function getTipIndex() {
  try {
    return JSON.parse(localStorage.getItem(TIP_INDEX_KEY) || '{}');
  } catch(e) {
    return {};
  }
}

function saveTipIndex(index) {
  localStorage.setItem(TIP_INDEX_KEY, JSON.stringify(index));
}

function getNextTip(suppName) {
  const tips = SUPP_TIPS[suppName];
  if (!tips || tips.length === 0) return null;

  const tipIndex = getTipIndex();
  const currentIndex = tipIndex[suppName] || 0;

  const tip = tips[currentIndex];

  // 다음 인덱스 저장 (순환)
  tipIndex[suppName] = (currentIndex + 1) % tips.length;
  saveTipIndex(tipIndex);

  return {
    tip: tip,
    current: currentIndex + 1,
    total: tips.length
  };
}
